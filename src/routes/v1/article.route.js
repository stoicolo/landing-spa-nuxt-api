const express = require('express');
const auth = require('../../middlewares/auth');
const validate = require('../../middlewares/validate');
const articleValidation = require('../../validations/article.validation');
const articleController = require('../../controllers/article.controller');

const router = express.Router();

router
  .route('/')
  .post(auth('manageArticles'), validate(articleValidation.createArticle), articleController.createArticle)
  .get(auth('manageArticles'), validate(articleValidation.getArticles), articleController.getArticles);

router
  .route('/user/:userId')
  .get(auth(), validate(articleValidation.getArticlesByUserId), articleController.getArticlesByUserId);

router
  .route('/id/:internalId')
  .get(auth(), validate(articleValidation.getArticleByInternalId), articleController.getArticleByArticleInternalId);

router
  .route('/id/')
  .patch(auth('manageArticles'), validate(articleValidation.updateArticle), articleController.updateArticle)
  .delete(auth('manageArticles'), validate(articleValidation.deleteArticle), articleController.deleteArticle);

module.exports = router;

