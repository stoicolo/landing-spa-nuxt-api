const express = require('express');
const auth = require('../../middlewares/auth-config');
const validate = require('../../middlewares/validate');
const widgetValidation = require('../../validations/widget.validation');
const widgetController = require('../../controllers/widget.controller');

const router = express.Router();

router
  .route('/')
  .post(auth(), validate(widgetValidation.createWidget), widgetController.createWidget)
  .get(auth(), validate(widgetValidation.getWidgets), widgetController.getWidgets);

router
  .route('/:widgetId')
  .get(auth(), validate(widgetValidation.getWidget), widgetController.getWidget)
  .patch(auth(), validate(widgetValidation.updateWidget), widgetController.updateWidget)
  .delete(auth(), validate(widgetValidation.deleteWidget), widgetController.deleteWidget);

module.exports = router;

