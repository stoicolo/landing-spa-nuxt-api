const express = require('express');
const auth = require('../../middlewares/auth-config');
const validate = require('../../middlewares/validate');
const quizValidation = require('../../validations/quiz.validation');
const quizController = require('../../controllers/quiz.controller');

const router = express.Router();

router
  .route('/')
  .post(auth(), validate(quizValidation.createQuiz), quizController.createQuiz)
  .get(auth(), validate(quizValidation.getQuizes), quizController.getQuizes);

router
  .route('/:quizId')
  .get(auth(), validate(quizValidation.getQuiz), quizController.getQuiz)
  .patch(auth(), validate(quizValidation.updateQuiz), quizController.updateQuiz)
  .delete(auth(), validate(quizValidation.deleteQuiz), quizController.deleteQuiz);

module.exports = router;

